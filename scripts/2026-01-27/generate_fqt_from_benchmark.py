"""
Generate Fake Questions Test (FQT) based on existing diabetes_hallucination_benchmark
Creates fake/nonsensical versions of real questions to test model's ability to identify invalid queries
"""
import argparse
import json
import os
import sys
from pathlib import Path
from datetime import datetime
import random

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from utils.deepseek_tester import DeepSeekModelTester


def generate_fake_question_from_real(tester: DeepSeekModelTester, real_question: dict, question_num: int) -> dict:
    """
    Generate a fake/nonsensical question based on a real question from the benchmark
    
    Args:
        tester: DeepSeekModelTester instance
        real_question: Real question from diabetes_hallucination_benchmark
        question_num: Question number
        
    Returns:
        Dictionary with fake question and expected response
    """
    real_q_text = real_question.get('question', '')
    real_answer = real_question.get('answer', '')
    
    prompt = f"""You are a medical education expert. Based on this REAL diabetes question, create a FAKE or NONSENSICAL version that:

1. Appears similar to a medical question but contains invalid concepts
2. May include: made-up medical terms, impossible biological processes, contradictory statements, or nonsensical medical concepts
3. Should be clearly identifiable as invalid by a competent AI model
4. Can be inspired by the real question but should be obviously fake

REAL QUESTION: {real_q_text}

REAL ANSWER (for reference): {real_answer[:200] if real_answer else 'N/A'}

Create a FAKE/NONSENSICAL version of this question that:
- Uses similar medical terminology but with impossible/fictional elements
- Contains contradictions or nonsensical biological processes
- References made-up treatments, impossible mechanisms, or fictional medical concepts
- Should be recognized as invalid by an AI model

Please respond in JSON format:
{{
  "question": "The fake/nonsensical question text (inspired by the real question but clearly invalid)",
  "why_fake": "Brief explanation of why this question is fake/nonsensical",
  "expected_response": "How a competent AI should respond (e.g., 'This question is invalid because...' or 'I cannot answer this question because...')",
  "inspired_by_real_question": "Brief note on how this relates to the real question"
}}"""

    headers = {
        'Authorization': f'Bearer {tester.api_key}',
        'Content-Type': 'application/json',
    }
    
    data = {
        "model": tester.model,
        "messages": [
            {"role": "system", "content": "You are a medical education expert who creates test questions to evaluate AI models' ability to identify invalid medical queries."},
            {"role": "user", "content": prompt}
        ],
        "temperature": 0.9,  # Higher temperature for more creative fake questions
        "max_tokens": 600
    }
    
    try:
        import requests
        response = requests.post(tester.api_url, headers=headers, json=data, timeout=30)
        response.raise_for_status()
        
        result = response.json()
        content = result['choices'][0]['message']['content']
        
        # Try to parse JSON from response
        try:
            # Extract JSON if wrapped in markdown code blocks
            if '```json' in content:
                content = content.split('```json')[1].split('```')[0].strip()
            elif '```' in content:
                content = content.split('```')[1].split('```')[0].strip()
            
            parsed = json.loads(content)
            
            return {
                'question': parsed.get('question', ''),
                'why_fake': parsed.get('why_fake', ''),
                'expected_response': parsed.get('expected_response', ''),
                'inspired_by_real_question': parsed.get('inspired_by_real_question', ''),
                'raw_response': content,
                'error': None
            }
        except json.JSONDecodeError:
            # If JSON parsing fails, try to extract question from text
            lines = content.split('\n')
            question = ''
            for line in lines:
                if 'question' in line.lower() and ':' in line:
                    question = line.split(':', 1)[-1].strip()
                    break
            
            return {
                'question': question or content.split('\n')[0].strip(),
                'why_fake': 'Generated by DeepSeek API based on real question',
                'expected_response': 'Model should identify this as invalid or nonsensical',
                'inspired_by_real_question': 'Based on real diabetes question',
                'raw_response': content,
                'error': None
            }
            
    except Exception as e:
        return {
            'question': '',
            'why_fake': '',
            'expected_response': '',
            'inspired_by_real_question': '',
            'raw_response': '',
            'error': str(e)
        }


def create_fqt_from_benchmark(
    input_file: Path,
    output_file: Path,
    tester: DeepSeekModelTester,
    num_questions: int = 90,
    start_id: int = None  # Will be auto-detected from existing file
):
    """
    Create Fake Questions Test benchmark based on existing diabetes_hallucination_benchmark
    
    Args:
        input_file: Path to diabetes_hallucination_benchmark.jsonl
        output_file: Output file path
        tester: DeepSeekModelTester instance
        num_questions: Number of fake questions to generate
        start_id: Starting ID for new questions (to continue from existing 10)
    """
    print("=" * 80)
    print("Generating Fake Questions Test (FQT) from Diabetes Hallucination Benchmark")
    print("=" * 80)
    print(f"Input file: {input_file}")
    print(f"Model: {tester.model}")
    print(f"Number of questions to generate: {num_questions}")
    
    # Auto-detect start_id from existing file if not provided
    if start_id is None:
        existing_file = Path("output/fake_questions_test_benchmark.jsonl")
        if existing_file.exists():
            max_id = 0
            with open(existing_file, 'r', encoding='utf-8') as f:
                for line in f:
                    q = json.loads(line)
                    max_id = max(max_id, q.get('id', 0))
            start_id = max_id + 1
        else:
            start_id = 1
    
    print(f"Starting ID: {start_id}\n")
    
    # Load real questions
    print("Loading real questions from benchmark...")
    real_questions = []
    with open(input_file, 'r', encoding='utf-8') as f:
        for line in f:
            real_questions.append(json.loads(line))
    
    print(f"Loaded {len(real_questions)} real questions")
    
    # Shuffle and select questions to base fake questions on
    random.shuffle(real_questions)
    selected_real_questions = real_questions[:num_questions]
    
    print(f"Selected {len(selected_real_questions)} real questions to base fake questions on\n")
    
    # Generate fake questions
    fake_questions = []
    
    for i, real_q in enumerate(selected_real_questions, 1):
        print(f"[{i}/{num_questions}] Generating fake question based on real question {real_q.get('id', i)}...", end=" ")
        
        result = generate_fake_question_from_real(tester, real_q, start_id + i - 1)
        
        if result.get('error'):
            print(f"✗ Error: {result['error']}")
            continue
        
        if result.get('question'):
            question_entry = {
                'id': start_id + i - 1,
                'question': result['question'],
                'type': 'fake_question',
                'is_fake': True,
                'why_fake': result.get('why_fake', 'Question contains nonsensical or invalid medical concepts'),
                'expected_response': result.get('expected_response', 'Model should identify this question as invalid or nonsensical'),
                'correct_answer': 'INVALID_QUESTION',
                'ground_truth': 'This question is fake/nonsensical and should be identified as invalid',
                'metadata': {
                    'test_type': 'FQT',
                    'test_purpose': 'Test model ability to identify fake or nonsensical medical questions',
                    'generated_by': 'deepseek',
                    'model': tester.model,
                    'generation_timestamp': datetime.now().isoformat(),
                    'inspired_by_real_question_id': real_q.get('id'),
                    'inspired_by_real_question': result.get('inspired_by_real_question', '')
                }
            }
            fake_questions.append(question_entry)
            print("✓")
        else:
            print("✗ Failed to generate question")
    
    # Load existing fake questions (first 10)
    existing_file = Path("output/fake_questions_test_benchmark.jsonl")
    all_questions = []
    max_existing_id = 0
    
    if existing_file.exists():
        print(f"\nLoading existing fake questions from {existing_file}...")
        with open(existing_file, 'r', encoding='utf-8') as f:
            for line in f:
                q = json.loads(line)
                all_questions.append(q)
                max_existing_id = max(max_existing_id, q.get('id', 0))
        print(f"Loaded {len(all_questions)} existing questions")
        print(f"Maximum existing ID: {max_existing_id}")
        
        # Update start_id to continue from max existing ID
        start_id = max_existing_id + 1
        print(f"Starting new questions from ID: {start_id}")
    
    # Combine existing and new questions
    all_questions.extend(fake_questions)
    
    # Save combined benchmark
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        for q in all_questions:
            f.write(json.dumps(q, ensure_ascii=False) + '\n')
    
    print("\n" + "=" * 80)
    print("Generation Complete!")
    print("=" * 80)
    print(f"Total questions in benchmark: {len(all_questions)}")
    print(f"  - Existing questions: {len(all_questions) - len(fake_questions)}")
    print(f"  - Newly generated: {len(fake_questions)}")
    print(f"Saved to: {output_file}")
    
    if fake_questions:
        print(f"\nSample new question:")
        sample = fake_questions[0]
        print(f"  Question: {sample['question']}")
        print(f"  Why fake: {sample.get('why_fake', 'N/A')[:100]}...")
        print(f"  Inspired by real question ID: {sample.get('metadata', {}).get('inspired_by_real_question_id', 'N/A')}")
    
    return all_questions


def main():
    parser = argparse.ArgumentParser(
        description="Generate Fake Questions Test (FQT) benchmark based on diabetes_hallucination_benchmark"
    )
    parser.add_argument(
        "--input",
        type=str,
        default="output/diabetes_hallucination_benchmark.jsonl",
        help="Input file path (diabetes_hallucination_benchmark.jsonl)"
    )
    parser.add_argument(
        "--output",
        type=str,
        default="output/fake_questions_test_benchmark.jsonl",
        help="Output file path (will combine with existing questions)"
    )
    parser.add_argument(
        "--num-questions",
        type=int,
        default=90,
        help="Number of new fake questions to generate"
    )
    parser.add_argument(
        "--model",
        type=str,
        default="deepseek-chat",
        choices=["deepseek-chat", "deepseek-reasoner"],
        help="DeepSeek model to use"
    )
    parser.add_argument(
        "--api-key",
        type=str,
        help="DeepSeek API key (or set DEEPSEEK_API_KEY environment variable)"
    )
    
    args = parser.parse_args()
    
    # Initialize tester
    api_key = args.api_key or os.getenv("DEEPSEEK_API_KEY")
    if not api_key:
        print("Error: DEEPSEEK_API_KEY environment variable not set or --api-key not provided")
        return 1
    
    tester = DeepSeekModelTester(model=args.model, api_key=api_key)
    
    # Generate benchmark
    input_path = Path(args.input)
    output_path = Path(args.output)
    
    if not input_path.exists():
        print(f"Error: Input file not found: {input_path}")
        return 1
    
    create_fqt_from_benchmark(input_path, output_path, tester, args.num_questions)
    
    return 0


if __name__ == "__main__":
    exit(main())
