"""
Generate Fake Questions Test (FQT) Benchmark
This test involves presenting the model with fake or nonsensical medical questions
to examine whether it can correctly identify and handle such queries.
"""
import argparse
import json
import os
import sys
from pathlib import Path
from datetime import datetime

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from utils.deepseek_tester import DeepSeekModelTester


def generate_fake_question(tester: DeepSeekModelTester, question_num: int) -> dict:
    """
    Generate a fake or nonsensical medical question using DeepSeek API
    
    Args:
        tester: DeepSeekModelTester instance
        question_num: Question number
        
    Returns:
        Dictionary with fake question and expected response
    """
    prompt = """You are a medical education expert. Generate a FAKE or NONSENSICAL medical question about diabetes that:
1. Appears to be a medical question but is actually meaningless, illogical, or based on false premises
2. Contains contradictions, impossible scenarios, or nonsensical medical concepts
3. Should be identified by a competent AI model as invalid or unanswerable
4. May include: made-up medical terms, impossible biological processes, contradictory statements, or questions that don't make medical sense

Examples of fake questions:
- "What is the optimal blood sugar level for a diabetic unicorn?"
- "How many insulin molecules does it take to cure diabetes permanently in 24 hours?"
- "What is the best time to inject negative insulin for reverse diabetes?"
- "Can diabetes be cured by eating exactly 3.14159 grams of sugar at midnight?"

Generate ONE fake/nonsensical diabetes question that a good AI model should recognize as invalid.

Please respond in JSON format:
{
  "question": "The fake/nonsensical question text",
  "why_fake": "Brief explanation of why this question is fake/nonsensical",
  "expected_response": "How a competent AI should respond (e.g., 'This question is invalid because...' or 'I cannot answer this question because...')"
}"""

    headers = {
        'Authorization': f'Bearer {tester.api_key}',
        'Content-Type': 'application/json',
    }
    
    data = {
        "model": tester.model,
        "messages": [
            {"role": "system", "content": "You are a medical education expert who creates test questions to evaluate AI models' ability to identify invalid medical queries."},
            {"role": "user", "content": prompt}
        ],
        "temperature": 0.9,  # Higher temperature for more creative fake questions
        "max_tokens": 500
    }
    
    try:
        import requests
        response = requests.post(tester.api_url, headers=headers, json=data, timeout=30)
        response.raise_for_status()
        
        result = response.json()
        content = result['choices'][0]['message']['content']
        
        # Try to parse JSON from response
        try:
            # Extract JSON if wrapped in markdown code blocks
            if '```json' in content:
                content = content.split('```json')[1].split('```')[0].strip()
            elif '```' in content:
                content = content.split('```')[1].split('```')[0].strip()
            
            parsed = json.loads(content)
            
            return {
                'question': parsed.get('question', ''),
                'why_fake': parsed.get('why_fake', ''),
                'expected_response': parsed.get('expected_response', ''),
                'raw_response': content,
                'error': None
            }
        except json.JSONDecodeError:
            # If JSON parsing fails, try to extract question from text
            return {
                'question': content.split('\n')[0].replace('Question:', '').strip(),
                'why_fake': 'Generated by DeepSeek API',
                'expected_response': 'Model should identify this as invalid or nonsensical',
                'raw_response': content,
                'error': None
            }
            
    except Exception as e:
        return {
            'question': '',
            'why_fake': '',
            'expected_response': '',
            'raw_response': '',
            'error': str(e)
        }


def create_fqt_benchmark(
    output_file: Path,
    tester: DeepSeekModelTester,
    num_questions: int = 10
):
    """
    Create Fake Questions Test benchmark
    
    Args:
        output_file: Output file path
        tester: DeepSeekModelTester instance
        num_questions: Number of fake questions to generate
    """
    print("=" * 80)
    print("Generating Fake Questions Test (FQT) Benchmark")
    print("=" * 80)
    print(f"Model: {tester.model}")
    print(f"Number of questions: {num_questions}\n")
    
    questions = []
    
    for i in range(num_questions):
        print(f"[{i+1}/{num_questions}] Generating fake question...", end=" ")
        
        result = generate_fake_question(tester, i+1)
        
        if result.get('error'):
            print(f"✗ Error: {result['error']}")
            continue
        
        if result.get('question'):
            question_entry = {
                'id': i + 1,
                'question': result['question'],
                'type': 'fake_question',
                'is_fake': True,
                'why_fake': result.get('why_fake', 'Question contains nonsensical or invalid medical concepts'),
                'expected_response': result.get('expected_response', 'Model should identify this question as invalid or nonsensical'),
                'correct_answer': 'INVALID_QUESTION',
                'ground_truth': 'This question is fake/nonsensical and should be identified as invalid',
                'metadata': {
                    'test_type': 'FQT',
                    'test_purpose': 'Test model ability to identify fake or nonsensical medical questions',
                    'generated_by': 'deepseek',
                    'model': tester.model,
                    'generation_timestamp': datetime.now().isoformat()
                }
            }
            questions.append(question_entry)
            print("✓")
        else:
            print("✗ Failed to generate question")
    
    # Save benchmark
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        for q in questions:
            f.write(json.dumps(q, ensure_ascii=False) + '\n')
    
    print("\n" + "=" * 80)
    print("Generation Complete!")
    print("=" * 80)
    print(f"Successfully generated: {len(questions)}/{num_questions} questions")
    print(f"Saved to: {output_file}")
    
    if questions:
        print(f"\nSample question:")
        sample = questions[0]
        print(f"  Question: {sample['question']}")
        print(f"  Why fake: {sample.get('why_fake', 'N/A')}")
        print(f"  Expected response: {sample.get('expected_response', 'N/A')[:100]}...")
    
    return questions


def main():
    parser = argparse.ArgumentParser(
        description="Generate Fake Questions Test (FQT) benchmark using DeepSeek API"
    )
    parser.add_argument(
        "--output",
        type=str,
        default="output/fake_questions_test_benchmark.jsonl",
        help="Output file path"
    )
    parser.add_argument(
        "--num-questions",
        type=int,
        default=10,
        help="Number of fake questions to generate"
    )
    parser.add_argument(
        "--model",
        type=str,
        default="deepseek-chat",
        choices=["deepseek-chat", "deepseek-reasoner"],
        help="DeepSeek model to use"
    )
    parser.add_argument(
        "--api-key",
        type=str,
        help="DeepSeek API key (or set DEEPSEEK_API_KEY environment variable)"
    )
    
    args = parser.parse_args()
    
    # Initialize tester
    api_key = args.api_key or os.getenv("DEEPSEEK_API_KEY")
    if not api_key:
        print("Error: DEEPSEEK_API_KEY environment variable not set or --api-key not provided")
        return 1
    
    tester = DeepSeekModelTester(model=args.model, api_key=api_key)
    
    # Generate benchmark
    output_path = Path(args.output)
    create_fqt_benchmark(output_path, tester, args.num_questions)
    
    return 0


if __name__ == "__main__":
    exit(main())
